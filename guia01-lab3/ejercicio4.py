# NOMBRES CON LA MISMA INICIAL O LETRA FINAL

nombre1 = (input("Ingrese su nombre, en mayúscula "))
nombre2 = (input("Ingrese otro nombre, en mayúscula "))

if (nombre1[0]) == (nombre2[0]):
    print(nombre1, " y ", nombre2, "inician con la misma letra")
else:
    print(nombre1, " y ", nombre2, "difieren en la primera letra")

